#1. upper case
a = "Hello, World!"
print(a.upper())
#2. lower case
a = "Hello, World!"
print(a.lower())
#3. remove wtitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"
#4. replace string
a = "Hello, World!"
print(a.replace("H", "J"))
#5. split string
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']