#1. Many values to multiple variables
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)
print("\n")

#2. One value to multiple variables:
x = y = z = "Orange"
print(x)
print(y)
print(z)
print("\n")

#3. Unpack a list:
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)
